def main():
    st.set_page_config(page_title="Directory Service Console", layout="wide")
    st.title("企業級網域目錄服務管理平台")
    
    with st.sidebar:
        st.header("系統功能")
        mode = st.selectbox("功能模式選擇", OP_MODES)

    if mode == "帳號自動化派發":
        st.subheader("員工到職帳號預先配置")
        
        container = st.container()
        col1, col2 = container.columns(2)
        
        with col1:
            emp_name = st.text_input("員工姓名", placeholder="輸入姓名")
            st.caption("擔任職位：正式員工")
            
        with col2:
            dept = st.selectbox("部門單位選擇", list(DEPT_MATRIX.keys()))
            site = st.radio("派駐地點", ["台北總部", "台中分公司", "高雄廠區"], horizontal=True)

        if emp_name:
            st.divider()
            st.success(f"系統已準備好 {emp_name} ({dept}) 的權限預校")
            st.subheader("🛡️ 權限清單")
            st.info("管理員操作介面：勾選項目即代表正式寫入該帳號之網域權限與檔案存取規範")
            
            perms = DEPT_MATRIX[dept]
            cols = st.columns(3)
            
            for i, p in enumerate(perms):
                with cols[i % 3]:
                    st.checkbox(p, value=True, key=f"perm_{emp_name}_{p}")
        else:
            st.warning("請輸入員工姓名以啟動配置。")

    else:
        st.subheader(f"目前進入：{mode}")
        st.info(f"正在連線至遠端伺服器獲取「{mode}」之相關數據...")
        st.write("---")
        st.write("功能開發中：此模組之實體權限連動需在公司內網環境下執行。")

if __name__ == "__main__":
    main()